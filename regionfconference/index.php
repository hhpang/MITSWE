<h3>About the Conference</h3>
<p>This year’s theme is <strong>Unlocking Global Opportunity:
Connections and Innovations for the Future</strong>. 
Globalization plays an important role in today's
society, and it is important for us to realize that we are
working and competing in a global setting. We will be
highlighting the technology and innovation that has
allowed for communication without borders through
keynote speakers, workshops, and other events.
We anticipate attendance of approximately 300
collegiate and professional members. The conference
will offer workshops targeted towards both audiences.</p>

<br>
<h3>About National SWE</h3>
<p>The Society of Women Engineers (SWE), founded in 1950, is a not-for-profit educational and service
organization. SWE is the driving force that establishes engineering as a highly desirable career aspiration
for women. SWE empowers women to succeed and advance in those aspirations and be recognized for
their life-changing contributions and achievements as engineers and leaders.</p>

<br>
<h3>About SWE Region F</h3>
<p>The Society of Women Engineers is divided into ten regions throughout the country. Region F, The
New England region, covers Maine, New Hampshire, Vermont, Massachusetts, Connecticut, Rhode
Island, and eastern New York. It has over 800 professional members from 9 different sections.</p>

