<center>
<h3>Johannah Segarich</h3>
<h4>Project Coordinator, Strings for Cambodia</h4>
</center>
<br>

<p>Johannah Segarich, Project Coordinator for Strings for Cambodia, is Professor of Music and Coordinator of the Music Department Outreach Program at Middlesex Community College.  She has been a speaker and panelist for numerous events in the Boston area, and was invited to speak at conferences in Sydney, Melbourne and Brisbane, Australia (2009) and Asian Studies Development Program National Conference in Boston (2010).  Also a mezzo-soprano, Ms. Segarich has performed widely throughout New England as a soloist in recital, opera and oratorio.</p><p>
Strings for Cambodia is a campaign to raise funding to send Western classical string instruments to the Royal University of Fine Arts in Phnom Penh, Cambodia.  The project grew out of a U.S. Department of Education Fulbright-Hays grant for study abroad in July, 2010 to promote research and communication between American and Cambodian educators.</p><p>
While in Cambodia, she met with RUFA faculty, administrators and students. “I learned of their desperate need for quality string instruments. The only instruments available to them now are old and of inferior quality. The carbon-fiber instruments will form the core of the professional-level orchestra and help inspire students to strive for excellence.  As the people of Cambodia emerge from the tragedy of the Khmer Rouge Genocide, they are struggling to rebuild their society on every level.  RUFA’s Music Department has two primary goals: To preserve and pass on traditional Cambodian music, and give Cambodia standing and recognition in the world through performances of Western classical music.”</P>

