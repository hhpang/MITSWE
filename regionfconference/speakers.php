<br>

<p>
Alyse Stofer has been active at all levels of SWE over her 18 years of membership, including as the Director of Regions in fiscal years 2009 and 2010, and the Director of Membership Initiatives in fiscal year 2011. She has also served as the Region H Governor, Lieutenant Governor, and Secretary, as well as in a variety of positions for SWE-Minnesota including section president, vice president, secretary and representative. In 2003 she was SWE's New Face of Engineering and featured in USA Today.  In 2006 she received the SWE Distinguished New Engineer Award and was also the recipient of the MFESTS (Minneosta Federation of Engineering, Science, and Technology Societies) Young Engineer of the Year Award.  In 2010, Stofer received the Region H Distinguished Service Award.
