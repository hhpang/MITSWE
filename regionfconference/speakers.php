<h3>Keynote Speaker: Natalie Givans</h3>
<h4>Senior Vice President at Booz Allen Hamilton</h4>
</center>
<br>

<p>
Based in Herndon, Virginia, Booz Allen Hamilton Senior Vice President Natalie Givans has over 26 years of experience in system security engineering, Cyber security, information and mission assurance, and communications, information and transmission security. Natalie is a senior leader in the firm's Cyber Technology Center of Excellence. 
</p><p>
Natalie is focused on three specific areas for the firm: leading cyber security and privacy offerings and solutions for the firm's Health market; driving technology offerings and solutions into the Military Health System; and driving the firm's overall service offerings and solutions into the Navy/Marine Corps and Army Health subaccounts. Her teams provide privacy and security services integrated into the firm's CMMI, System Engineering and Acquisition life cycles. Four major service offerings include Security Programs, Policy and Compliance; Security Architecture and Engineering; Cyber Security Operations; and Mission Assurance/Enterprise Resilience.  Ms. Givans was an active member of the System Security Engineering Capability Maturity Model (SSE-CMM) project, focused on ensuring that security is integrated into all acquisition and systems development lifecycles from their conception and was an author of the SSE-CMM, which obtained its approval as ISO Standard 21827.
</p><p>
Natalie is a member of the Booz Allen Excellence Awards Committee and the Women's Leadership Initiative.  She was named Engineer of the Year by the District of Columbia Council of Engineering and Architectural Societies for 2009.
</p><p>
Natalie is the Chair of the Board of Directors for AFCEA International.  She has been a member for 25 years, serving in a variety of roles including member of the Board of the Educational Foundation, member of the Board of the Building Services Corporation, and director of the Board of Directors.  She is also a member of the MIT Alumni Association Board of Directors, serving on the Finance Committee and establishing an Ad Hoc committee on Energy, Environment and Sustainability.  She is also the firm's Partner Sponsor for the MIT Industrial Liaison Program, the Society of Women Engineers and its Corporate Partnership Council, and the Girl Scouts of America including the Making the Connection program.  She was the first Partner sponsor for the firm's Parent's Forum.  She is the Pack Committee Chair for Cub Scout Pack 673.
</p>

