Stofer is an Engineering Program Manager at Medtronic, the global leader in medical technology devices. She is leading product development projects in the Neuromodulation business sector from concept to global commercialization.  During her two years at Medtronic, she has launched two new products to assist with alleviating chronic back and leg pain. Prior to Medtronic, Stofer was employed at Transoma Medical for 11 years where she was a program manager leading the continuation engineering and product improvement engineering teams for the Patient Management Device division. For much of her career, Stofer has contributed to and led technical design teams, and as a result is a co-inventor on several pending patents and is a co-inventor on patent number 7,344,505.
</p><p>
Alyse received her Bachelor's degree in biomedical engineering from the University of Iowa, her Master's degree in biomaterials from the University of Minnesota, and a medical device certificate from the University of St. Thomas. She and her husband Kevin have a young son and daughter. She enjoys spending time outside with her children, as well as reading and baking. 
