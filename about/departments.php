<h4>The following is a brief description of the different departments in MIT SWE.</h4>
<br>
<p><h3>Campus Relations</h3></p>
<p>
Campus Relations forges the tie between MIT SWE and the rest of the MIT community. The department
is responsible for the planning of events that build professor-student relations and peer-to-peer
relations among students as well. Board members of this department work closely with various
academic departments at MIT and other student organizations to promote collaboration and active
engagement on campus.
</p>
<br>
<p><h3>Corporate Relations</h3></p>
<p>
Corporate Relations manages MIT SWE's communication with corporate representatives and companies, in
addition to providing members with a wide variety of career-related resources and events. Among these
events are site tours, individual company events, career panels, information sessions, networking events, 
and career development workshops. The department also oversees the annual Career Fair Banquet that is held
during Career Week in conjunction with MIT's Fall Career Fair.
</p>
<br>
<p><h3>Membership</h3></p>
<p>
Membership focuses its efforts on providing resources to members of MIT SWE, who are also
required to be national members of SWE, as well as promoting individuals to become members. Important
responsibilities that this department holds include hosting MIT Orientation and CPW events for MIT SWE, 
planning logistics for SWE National and Regional Conferences, and helping run General Body Meetings.
</p>
<br>
<p><h3>Outreach</h3></p>
<p>
Outreach connects MIT SWE to the local community through its broad range of programming for young girls 
and women interested in the STEM fields. Some of these are year-long programs, such as Keys to Empowering Youth 
(KEYs) and Women in Science and Engineering (WiSE), while others occur on an event-to-event basis. The 
department has strong ties with local Girl Scouts and Brownie Scouts troops, as well as relations with 
schools in the Greater Boston Area.
</p>

</p>


