<h3>What is Design Squad?</h3>
Design Squad is a 10-week design thinking apprenticeship held at the Orchard Gardens Citizen Schools to help students 
use design thinking and engineering to make a difference in their community. Students will work in groups and finish 
the program with a finalized product to solve a problem, and these products will be presented at a final WOW presentation.
<br><br>
<h3>When does the program take place?</h3>
Sessions are held once a week on <b>Tuesday</b> afternoons, from 2:30pm to 5:30pm.<br><br>
<h3>How do I get involved?</h3>
Volunteers should be prepared to mentor from 2:30pm to 5:30pm (this includes transportation time). All majors, 
backgrounds, and gender are welcome. This is a fantastic opportunity for MIT SWE members to have fun with kids and 
gain planning experience, especially for those interested in being on a SWE committee or the planning board. If 
interested, please contact <a href="mailto:swe-engineering-outreach@mit.edu">SWE Engineering Outreach</a>.
