<h3>What is MIT@Lawrence?</h3>

<p>SWE is proud to collaborate with the MIT@Lawrence Program to provide science 
enrichment programs for middle school students from Lawrence, MA. The goal of the program is to 
raise high school graduation rates as well as to the college attendance rate through encouraging
young minds in exploration of science, engineering, and mathematics.</p>

