<h3>Program Updates</h3>
<p>During the last session in Spring 2011, the students participated in experiments 
involving food. First, the students conducted an experiment exploring the properties 
of milk and detergent, and learned about polarity and surface tension. Next, the 
students were introduced to color chromatography, and also played with tonic water and
a blacklight to explore light scattering. To bring relevance into the experiments, the
students then engaged in a discussion of the possibilities and challenges in pursuing a 
career in science, technology, engineering, and mathematics.</p>

