<h3>What is #helloworld?</h3>

<p>Based on last year's Web Design Competition, #helloworld is a new program run by MITÕs Society of Women Engineers (SWE) to teach middle school girls the basics of web design. The goal is to introduce girls to coding with HTML and CSS and spark their curiosity in computer programming and computer science. Each student will have the opportunity to create their own website on a STEM topic of their choosing, and each student will be paired up with a MIT student volunteer who will be their mentor during the program.</p>

<p>The program involves six <b>three-hour sessions</b> from <b>October 14, 2012</b> to <b>November 25, 2012</b>.</p> <b>Please note the change to program dates.</b>

<p>The first session will be from 12:30 - 3:30 PM and will include lunch and a brief meet and greet for the students and their mentors. The following five sessions will consist of a lesson in web design, followed by students working on their websites, and will all run from 2 - 5 PM. The final session will also be from 12:30 - 3:30 PM and include lunch, and each student will give a short presentation and have a chance to show off their website and hard work! Parents are invited to both the first and last sessions.</p>

<p>So in summary, the program dates and times are: <br>
Oct 14th: 12:30 - 3:30 PM<br>
Oct 21st - Nov 18th: 2 - 5 PM<br>
Nov 18th: 12:30 - 3:30 PM<br></p>

<p>This program is entirely free and will be held on MIT's campus and run by MIT student volunteers. We will provide snacks for the girls, and their hard work and participation will culminate in a fully functional website that can be shown off to family and friends!</p>


