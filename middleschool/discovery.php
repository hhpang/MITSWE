<p>

<h3>What is Discovery UnliMITed?</h3>

<p>Discovery UnliMITed is a new STEM program held by MIT Society of Women Engineers to introduce middle school girls (grades 6-8) to simple but cool experiments and projects. The goal is to inspire girls to explore the exciting world of STEM and ultimately spark their curiosity in these fields by showing them that there is more to science and math than just reading textbooks in class! We hope to encourage interest in these fields through fun hands-on activities, with the help of MIT student volunteers.</p>

<p>The program involves <b>five two-hour sessions</b> on Sundays from <b>1:30-3:30 PM</b> starting <b>September 28, 2014</b> and ending <b>October 26, 2014</b>. Our sessions are themed around different STEM fields: Mechanical Engineering, Computer Science, Civil Engineering, Electrical Engineering, and Architecture.</p>
<p>

The individual sessions will be hosted on:<p>
<b>September 28th</b> Architecture + Civil Engineering<br>
<b>October 5th</b> Electrical Engineering<br>
<b>October 12th</b> Energy + Materials Science <br>
<b>October 19th</b> Computer Science and Programming Part 1<br>
<b>October 26th</b> Computer Science and Programming Part 2<br>
</p>

<p>The sessions will be held on MIT's campus and run by MIT student volunteers. We will provide snacks for the girls, and activities will end in items and ideas that can be taken home to show off to friends and family!</p>

