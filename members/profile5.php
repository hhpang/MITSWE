<p>
					In the 10 years since I joined Draper Laboratory, I have had the opportunity to help design robots that swim, fly, walk, roll, and slither.   For the last seven years, I have been the group leader for the Vehicle & Robotic Systems group where we build first-of-a-kind robots for niche applications.  These have included robotic fish, high-g survivable air vehicles, micro-aerial vehicles, and rolling, tracked, legged, and snake-like robots.   Our projects are always changing which keeps my work interesting.<br><br>
					My day-to-day activities are divided between technical projects, management activities, and business development.  This includes going to visit sponsors and hosting them at Draper, as well as writing proposals for government funding. I manage 10 regular employees and 3 graduate students, and oversee the Autonomous Vehicle Laboratory.  I often attend conferences and workshops covering a wide variety of technical and government program subjects. <br><br>
					It is amusing how my career ambitions changed from when I first entered college at the University of California, San Diego.  Originally planning to study marine biology, I ended up in mechanical engineering by pure serendipity.   In graduate school, I tied together my interest in marine science and mechanical engineering by earning my master's and doctorate degrees in oceanographic engineering through the MIT/Woods Hole Oceanographic Institution Joint Program.  For my doctoral research, I studied the fish swimming hydrodynamics and applied that knowledge to propulsion systems for submersibles.<br><br>
					I was able to extend that work by joining Draper as a postdoctoral fellow. My first project was the design and development of an autonomous robotic tuna, for which I was the principal investigator.  It was particularly satisfying to help engineer a system that literally began with a fish, and ended with a freely swimming 300 lb robot. <br><br>
					The robotic tuna project was funded internally by Draper. One of the unique strengths of Draper is our robust independent research and development program, through which we get to champion and incubate new technologies.<br><br>
					In addition to providing interesting projects, Draper has been flexible about scheduling. The fact that I've been able to reduce my hours to 80% of full time for the last six years has been an important aspect of my job since the birth of my children. It helps me to maintain balance between career and personal needs.<br><br>
					The Laboratory's emphasis on support of technical education has also given me the chance to mentor students. As they do hands-on work in Draper's Autonomous Vehicle Lab, I get to share my professional interests as they explore their own. This adds another dimension of satisfaction to my work at Draper Laboratory.<br><br>
</p>

