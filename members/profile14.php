<p>
I work as a researcher and business development engineer at Aurora Flight Sciences (an odd combo of jobs, but sometimes working at a small company allows you to wear two hats.)  That means that I spend about half my time helping to write technical proposals for projects that other engineers will eventually carry out, and about half my time working on technical projects myself.  It's a good mix.  My projects include a systems engineering analysis of planetary rover technologies and architectures, development of a biomedical sensor for space use, and research on a space suit simulator for earth-based training and experiments.
</p><p>
Industry (in my experience) requires the ability to rapidly switch between tasks.  I may come in one morning, sort through email, then settle down to writing an interim report on a project I'm working on, when I get an email from a colleague that he'd like to submit a proposal for an opportunity, which is due in a week and a half, and he needs help finding writers, pricing the proposal, developing a work plan, and actually writing the text.  Whew!  My report gets dropped while I work on that, at least until I have to attend a previously scheduled status meeting for another technical project I'm working on. Of course, these tasks are also smattered with various "fillers" … contacting the IT guy about a software update, reviewing technical material on our internal website, and yes, writing up a Day In The Life narrative (don't ever believe someone who says their job involves no "fillers"!) 
</p><p>
Here is what I wish I had known.  (1) Everyone in engineering gets labeled with a technical expertise, even if they have a fairly general background.  So, you might as well pick your best/favorite types of classes (e.g. aerodynamics, controls, design) and call that your expertise.  That does NOT mean slack off in your other classes! (2) At some point in your career, you may have the opportunity to continue doing mostly technical work, or to go the "management" route.  You don't need to decide now, but be aware of both possibilities, and think about it as you start your career.  (3) At a good company, your gender has absolutely no bearing, positive or negative, on your ability to perform, excel, and advance.  Don't ever use it as an excuse (to yourself or to your boss.)  (4) Work more efficiently, not longer.  In the workplace especially, it is possible to have very reasonable hours if you plan ahead, prioritize your tasks, and don't overcommit.  This is actually possible in school too, though you probably don't believe me.  (5) A career is a marathon, not a sprint.  There is no "graduation" from work (well, retirement, but that's a long way away).  Settle into a routine and work environment that will be acceptable to you long term, so that your career is part of a balanced life.
</p><p>
Good luck!
