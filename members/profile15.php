<p>
The “BEEP – clank” of the BART gate signals the start of the typical San Franciscan commute. While the morning fog has largely been burned away by the hot California sun and the majority of workers are quietly typing away in their cubicles, I find myself at 11am stepping onto the train to work and thinking about how to finish implementing a feature I started the day before, completely undistracted by the screeching brakes and street musicians playing out-of-tune guitars.
</p><p>
I exit the train at the Powell station, in the heart of San Francisco and right near the Dropbox office. I ride the elevator to the top floor, key into Dropbox, and head directly to the kitchen for my homemade morning latte. The office has a great social atmosphere, so I joke around with people as my coffee brews.
</p><p>
I return to my desk with a half-empty latte. It’s game time. I put on my headphones and enter a world of Objective-C iPhone programming. When I first started at Dropbox, which was 3 months ago, I had never done any iPhone development. Working here forces me to expand my skillset rapidly and dramatically. Luckily, there are always people willing to help, and there is a collaborative environment where everyone is always eager to learn from each other.
</p><p>
Working on a new feature for our iPhone app, I discover a corner case not covered by the design. I shout out to the design team, as they sit right across from me, and find out the Android team has just encountered the same problem. A discussion starts right then, and we flesh out a solution on the spot.
</p><p>
The gong rings at noon, signaling lunch. People slowly but surely migrate to the kitchen for nicely catered food ordered from local eateries. Lunch is a great opportunity to get to know people. We talk about what we did over the weekend; exciting news in the world; Dropbox related stuff; really anything people have on their minds. Conversations flow with interesting people, and after about an hour everybody begins to drift back to their desks.
</p><p>
Afternoons tend to fill up with engineering interviews, as a big part of working for a successful start-up is helping the company expand. I like that interviewing helps improve my ability to interact, understand, and assess people.
</p><p>
At 6pm sharp, I head over to the gym right across the street. As much as I love Dropbox, I like to get a change of scenery, and luckily the culture is very conducive to working whatever hours you like as long as you get the job done.
</p><p>
At 8pm I come back to the office for dinner. There are usually fewer people present in the kitchen at dinner because people eat at different times. It’s still really fun, as it allows for more one-on-one conversations.
</p><p>
Full of energy, I follow dinner with an intense coding session. Every now and then, I get distracted by DDR, Dance Central, and ping-pong. There’s an arcade-size DDR machine and a ping-pong table, so it’s tough to say no when somebody challenges me!
</p><p>
Once the city and the office quiet down, it’s time for me to head out. I catch the bus going from downtown to the Mission district so I can go home, get some sleep, and prepare myself for another exciting day. 
</p>

