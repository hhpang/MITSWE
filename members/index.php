<p>While national SWE members at MIT who aren't serving on our board aren't required to participate 
in all of these activities, a lot of them are great ways to get involved, meet new people, and 
learn more about SWE! This is also a good way to learn more about what board positions are like 
if you're interested in applying for one. We encourage you to:</p>

<p><ul style="margin-left: 30px;">
<li>Attend planning board meetings - they are open to national members!</li>
<li>Attend SWE events like social events, networking events, professional development events, and personal development events</li>
<li>Volunteer for Outreach events, the SWE booth, the MIT Career Fair, and other events SWE is hosting</li>
<li>Check out our calendar to see what's coming up next</li>
</ul></p>

<p>We hope to see you at our events in Spring 2014!</p>

