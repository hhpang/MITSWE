<h3>MIT SWE Member Expectations</h3>

<p>MIT SWE Board members, as leaders in the MIT SWE community, are expected to uphold certain 
commitments as a part of their positions.  These activities help to support the growth and 
vitality of the MIT SWE Section, and should also further your personal development as leaders.
These are our expectations for the SWE Board:</p>

<p><ul style="margin-left: 30px;">
<li>Attend all board meetings (~6 per semester)</li>
<li>Volunteer for at least two Outreach events per semester</li>
<li>Attend at least one Corporate Relations event per semester</li>
<li>Attend at least one SWE social event per semester</li>
<li>Sign up for one booth shift per semester (Orientation, CPW, SWE Gear booth, etc.)</li>
<li>Volunteer for the MIT Career Fair</li>
<li>Attend any MIT-hosted SWE conferences and volunteer for one shift</li>
</ul></p>

