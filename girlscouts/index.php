<h3>What is Girl Scouts Outreach?</h3>

<p>
The Girl Scouts outreach committee hosts two series of events for Brownie Girl Scouts and Junior Girl Scouts. Three morning-long sessions will be held each semester, with each session consisting of fun hands-on experiments such as extracting DNA from strawberries and straw bridge building. Brownie Girl Scouts Day is held in the fall semester, and Junior Girl Scout Day is held in the spring semester.</p>

<h3>Brownie Girl Scout Days</h3>
Brownie Girl Scout Days is a three session program which will take place in <b>Fall 2014</b> for Brownie Girl Scouts in the Boston area. Each hands-on, interactive session focuses on learning about a different topic with your fellow Junior Girl Scouts.  These events will consist of science and engineering based activities, related to outer space, chemistry, and sustainable energy.  Feel free to join us for one, two, or all three sessions.<p><p>

Brownie Girl Scout Days will take place on MIT's campus in room 4-149.  These events will take place from 10 AM - 2 PM, and will include lunch and snacks.  We can't wait to see you all there!

<h3>Junior Girl Scouts</h3>
<p>Junior Girl Scout Days is a three session program which will take place in <b>Spring 2015</b> for Junior Girl Scouts in the Boston area. Each hands-on, interactive session focuses on learning about a different topic with your fellow Junior Girl Scouts. Feel free to join us for one, two, or all three sessions.  Check back in early spring for more information about our Junior Girl Scout Days!<p>

