<p>
What's that in the sky? It's a bird, it's a plane, it's...a RoboMonkey? It's year 2020 and RoboMonkeys have come from 
outer space with just one mission: to take over Earth! And, as our top agent, it is your job to stop them. Come to our
secret headquarters and we will teach you how to design your own projectile launcher to stop them! But be warned, you will
only have popsicle sticks, tape, marshmallows, and other miscellaneous objects to accomplish this task. Good luck. 
We are counting on you.
</p>

<p>
At this workshop, students will learn the engineering and design process by building projectile launchers. Students
will be working in groups of 5-6 to create the best launcher possible. Not only will they learn to brainstorm ideas, 
but they will also design and construct their launchers, and testing them in an ultimate final competition! We hope
that through our workshop, we can inspire kids and make them more interested in designing and building. 
</p>

<p>
This workshop will be held on April 21st from 2-5pm at the MIT Museum first floor. Registrations are on a first come
first serve basis. Only 40 spots are available, so sign up fast! To register, fill out 
