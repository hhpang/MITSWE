<p>
Light up your creative spark with LED art! Come join MIT SWE with making anything you want to glow, from drawings to greeting cards.
There will be fun-colored LEDs, colorful cardstock, and plenty of markers. Come discover your inner passion for art and science through this
activity that you do not want to miss out on!
</p>

<p>
MIT SWE will be holding a booth during the annual Cambridge Science Festival. This year, the booth will be on April 19
from 12-4pm at the Cambridge Public Library in the Game Corner. Students in elementary school or middle school will have the 
opportunity to experience the magic behind circuits while making fun greeting cards with LEDs in them! Materials include 3V batteries,
copper wire, LED Stickers (created by our very own MIT Media Lab), and various art supplies. As students create their art, we will
go over some basic circuit concepts, including current, voltage potentials, conductors, and insulators.
</p>

