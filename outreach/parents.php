Outreach is a huge part of MIT's chapter of the Society of Women Engineers. Our programs reach over 800 kids age K-12 annually from diverse backgrounds.

<p><h3>Why Outreach?</h3>

Almost every student at MIT has an influential mentor or role model that inspired their career in science and engineering. We know very well the importance of these role models and extracurricular activities, and that pivotal moment when a participant realizes that science is not only fun, interesting, but actually really easy! We strive to make opportunities to explore science widely available, to equalize exposure to science topics basic and advanced across economic classes and social backgrounds.</br>
