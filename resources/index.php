<p><h3><a href="http://careers.mit.edu/">MIT Career Development Center (CDC) General Services</a></h3></p>

<p>
<h4><img src="/images/expand.gif">&nbsp;&nbsp;<a href="http://web.mit.edu/career/www/services/counsel.html">One-on-One Appointments with Counselors</a></h4>
</p>
The best way to receive help on a job search is to visit a counselor at the careers office! 
Talk to someone about any aspect of your career development such as resume writing editing or 
summer internship searching and find out how you can be more effective in your career search. 
Walk-in appointments are also available.			         

<p>
<h4><img src="/images/expand.gif">&nbsp;&nbsp;<a href="http://web.mit.edu/career/www/services/workshops.html">Career Planning Workshops</a></h4>
</p>
Do you want to ace your next interview? Learn how to network? Or write a winning resume and cover letter?
There are only some of a number of workshops offered by the careers office throughout the year! Check out 
the link for more details about dates and times. If you're interested but don't have the time, there are 
also online workshops that you can take.

<p>
<h4><img src="/images/expand.gif">&nbsp;&nbsp;<a href="http://web.mit.edu/career/www/guide/">Tip Guides</a></h4>
</p>
Find tutorials and quick tips on topics such as writing resumes, networking, and interviewing that will make your
job search more successful! Here you will also find nice overview descriptions of industry fields 
such as biotechnology and consulting.

<br><br>
<p><h3>Finding Internship Opportunities</h3></p>

<p>
<h4><img src="/images/expand.gif">&nbsp;&nbsp;<a href="http://web.mit.edu/education/">Departments of All Offered Majors at MIT</a></h4>
</p>
A great way to find internships is to sign up for your respective course department listservs 
(i.e. Anne Hunter for Course 6). Click to find resources for the course and department emails
you are interested in!

<p>
<h4><img src="/images/expand.gif">&nbsp;&nbsp;<a href="http://web.mit.edu/career/www/jobs/interviews.html">InterviewTrak</a></h4>
</p>
Browse through thousands of internship and job listings from the most sought-after companies! All on-campus
recruiters post their job offers to this site as well. Create an account to store your own resume online and 
submit them to companies! If selected by the company, you can also sign up for interviews here!

<br><br>
<p><h3>Networking</h3></p>	      

<p>
<h4><img src="/images/expand.gif">&nbsp;&nbsp;<a href="http://alum.mit.edu/cs/ican/index.html/">Institute Career Assistance Network (ICAN)</a></h4>
</p>
The crucial and most valuable link to alumni! This network provides students the support 
for networking and career adivsing. Search through and view the profiles of alumni who may 
work at the companies in which you have interest. Individuals specially marked "Career Advisors" 
have voluntarily offered their assistance and are enthusiastic to help out students with questions!

<p>
<h4><img src="/images/expand.gif">&nbsp;&nbsp;<a href="http://web.mit.edu/career/www/guide/networking.pdf/">All about Networking</a></h4>
</p>
Networking --- how exactly do you do it? Learn the how-to's by reading this quick overview about the
tips you can follow to gain more "connections" that will prove valuable in helping you and your future career.

