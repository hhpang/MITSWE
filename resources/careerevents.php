<h3>Past Events</h3>

<h4>Lunch with Professor Grimson</h4>
		
<p>We invited SWE to come and eat lunch with the new MIT Chancellor. We discussed what are the good and 
bad parts of MIT as we see it, like great that we have Pass/No record, it would be better to have a more 
unifying MIT feeling because right now it is very divided up by departments. We gave him suggestions to 
have lunched like this one with students and send invites to random MIT students to meet at, for example 
New Ashdown and talk about life and MIT.</p>

<h4>Women on Wall Street - Morgan Stanley</h4>
		
<p>Women on Wall Street was a down-to-earth, 1.5 hour panel with Key Note speaker Jody Leper-Kay from the 
technology department of Morgan Stanley and panelists including Arun Gundurao. The panelists did not lecture, 
but had conversations with the audience about topics such as what opportunities await students with science 
degrees, the mechanics of the finance world, and the difficult question of how to balance a one's career with
 a personal life as a woman. (You can find photos on the SWE homepage!)</p>

<h4>Apple App Showcase Event</h4>
		
<p>Apple representatives came to campus and held a grassroots iOS app presentation where MIT students showed 
off their interesting innovations on Apple computers in the Stata center while passerby MIT students could 
try them out. It was great advertising opportunity for the inventors and was very well received.</p>

