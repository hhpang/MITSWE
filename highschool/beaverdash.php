<h3>What is the BeaverDash?</h3>
<p>BeaverDash is a one-day mystery engineering challenge held once a year for both male and female high school students! 
This annual design competition pits teams of 4-5 students against one another in an intense battle of 
fast-paced design and thought. BeaverDash is an opportunity for local high school students to learn 
about practical engineering techniques while practicing innovative and creative thinking. 
An MIT undergraduate student is assigned to each team to mentor and guide the teams while raising 
awareness of engineering fields and disciplines.</p>
