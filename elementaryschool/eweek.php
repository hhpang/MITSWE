<h3>What is the Engineering Week?</h3>

<p>Engineering Week is a week-long celebration of hands-on science and engineering 
exploration at the Boston Children's Museum. Every year, SWE hosts a myriad of fun 
activities for the boys and girls of the greater Boston and Cambridge area. The 
booths are staffed by enthusiastic MIT students. Past activities have included 
explorations of the chemical properties of home-made slime, non-newtonian fluids,
 DNA from (one's own) cheek cells, and the strength of straw and rubber bands. 
Engineering Week occurs during mid- to late February every year.</p>

