<h3>What is the Mason Magellan Program?</h3>
The Mason Magellan Program is an after school program for students in grades 1-3 at the Mason Elementary School. We run weekly science activities with the students to encourage an interest in science, technology, engineering, and math in a fun environment. We also hope to offer similar programs at other schools throughout Boston, so if you are interested, please contact <a href="swe-outreach@mit.edu">swe-outreach@mit.edu</a>!
<br><br>
<h3>When does the program take place?</h3>
Sessions are held once every week this semester on <b>Wednesday</b> afternoons, from 3:00pm to 5:30pm.<br><br>
<h3>How do I get involved?</h3>
Volunteers should be prepared to mentor from 3:00pm-5:30pm (this includes transportation time).
All majors, backgrounds (and gender) are welcome. This is a fantastic opportunity for MIT SWE members
 to have fun with kids and gain planning experience, especially for those interested in being on a 
SWE committee or the planning board.
If interested, please contact <a href="mailto:swe-off-campus-outreach@mit.edu">Off-Campus Outreach</a>.
