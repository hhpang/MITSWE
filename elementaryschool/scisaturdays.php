<h3>Science Saturdays</h3>
Science Saturdays is a weekend program designed to bring fun science activities and demonstrations to the Cambridge Public Library. From building balloon rockets to solving miniature "crime scene" investigations, Science Saturdays accommodates a wide range of topics through lots of science fun. Organized in a booth-style, students are welcome to explore any of the activity booths and find out what is most interesting to them. Young scientists are able to discover the wonders of science in an informal setting that is fun and educational!

<br><br>
<h3>Why come to Science Saturdays?</h3>
Science Saturdays is a great way to get kids interested in science outside of school. During our events, we focus on giving kids the opportunity to discover new phenomena by letting them be the scientists for their own experiments. These activities range from creating color-changing flowers to experimenting with pop rocks and soda. By engaging them in hands-on activities, we hope to spark an interest in science early on so that more kids will pursue science and technology in the future.<br><br>
<h3>Who is this program geared towards?</h3>
